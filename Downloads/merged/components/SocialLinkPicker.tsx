interface SocialLinkPickerProps {
  onAdd: (title: string, url: string, icon: string) => void;
  disabled?: boolean;
}

export default function SocialLinkPicker({ onAdd, disabled }: SocialLinkPickerProps) {
  const [selected, setSelected] = useState<string | null>(null);
  const [url, setUrl] = useState("");

  const network = SOCIAL_NETWORKS.find((n) => n.id === selected);

  const handleAdd = () => {
    if (!network || !url.trim()) return;
    onAdd(network.name, url.trim(), network.id);
    setSelected(null);
    setUrl("");
  };

  return (
    <div className="space-y-3">
      <p className="text-[10px] font-bold text-muted-foreground uppercase">Quick Add Social</p>
      <div className="flex flex-wrap gap-2">
        {SOCIAL_NETWORKS.map((net) => (
          <button
            key={net.id}
            onClick={() => setSelected(selected === net.id ? null : net.id)}
            className={`flex items-center gap-1.5 px-3 py-2 rounded-lg text-xs font-bold border transition-all ${
              selected === net.id
                ? "border-primary bg-primary/10 text-primary"
                : "border-border bg-secondary/50 text-muted-foreground hover:border-primary/50"
            }`}
          >
            <span style={{ color: net.color }}>{net.icon}</span>
            {net.name}
          </button>
        ))}
      </div>
      {selected && network && (
        <div className="flex gap-2 animate-in slide-in-from-top-2">
          <Input
            value={url}
            onChange={(e) => setUrl(e.target.value)}
            placeholder={network.placeholder}
            className="flex-1"
          />
          <button
            onClick={handleAdd}
            disabled={!url.trim() || disabled}
            className="px-4 py-2 bg-primary text-primary-foreground font-bold text-xs rounded-lg hover:bg-primary/90 disabled:opacity-50"
          >
            <Plus className="w-4 h-4" />
          </button>
        </div>
      )}
    </div>
  );
}

